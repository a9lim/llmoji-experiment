def _plot_2d(*, coords_m, q_m, coords_s, q_s, coords_t, q_t,
             cent_m, cent_s, cent_t, var,
             out_path, short_name, which, n_m, n_s, n_t):
    import matplotlib.pyplot as plt
    from matplotlib.lines import Line2D
    from matplotlib.patches import Patch

    _use_cjk_font()
    fig, axes = plt.subplots(1, 2, figsize=(16.0, 7.6))

    panels = [(0, 1), (1, 2)]
    for ax, (i, j) in zip(axes, panels):
        for q in QUADRANT_ORDER_SPLIT:
            mask = q_m == q
            if np.any(mask):
                ax.scatter(coords_m[mask, i], coords_m[mask, j],
                           s=8, alpha=0.35, marker="o",
                           color=_color_for(q), edgecolors="none")
        for q in ALL_CELLS_ORDER:
            mask = q_s == q
            if np.any(mask):
                ax.scatter(coords_s[mask, i], coords_s[mask, j],
                           s=10, alpha=0.55, marker="^",
                           color=_color_for(q), edgecolors="none")
        for q in ALL_CELLS_ORDER:
            mask = q_t == q
            if np.any(mask):
                ax.scatter(coords_t[mask, i], coords_t[mask, j],
                           s=11, alpha=0.65, marker="s",
                           color=_color_for(q), edgecolors="none")

        for q in ALL_CELLS_ORDER:
            color = _color_for(q)
            cm = cent_m.get(q)
            cs = cent_s.get(q)
            ct = cent_t.get(q)
            if cm is not None and cs is not None:
                ax.plot([cm[i], cs[i]], [cm[j], cs[j]],
                        color="#888888", lw=0.8, alpha=0.7, zorder=2)
            if cm is not None and ct is not None:
                ax.plot([cm[i], ct[i]], [cm[j], ct[j]],
                        color="#1a5f6f", lw=1.1, alpha=0.85, zorder=2)
            if cm is not None:
                ax.scatter(cm[i], cm[j], s=120, marker="o",
                           edgecolors="black", facecolors=color,
                           linewidths=1.0, zorder=3)
            if cs is not None:
                ax.scatter(cs[i], cs[j], s=140, marker="^",
                           edgecolors="black", facecolors=color,
                           linewidths=1.0, zorder=3)
            if ct is not None:
                ax.scatter(ct[i], ct[j], s=150, marker="s",
                           edgecolors="black", facecolors=color,
                           linewidths=1.0, zorder=3)

        ax.axhline(0, color="#bbbbbb", lw=0.6, zorder=0)
        ax.axvline(0, color="#bbbbbb", lw=0.6, zorder=0)
        ax.set_xlabel(f"PC{i+1} ({var[i]*100:.1f}% var)")
        ax.set_ylabel(f"PC{j+1} ({var[j]*100:.1f}% var)")
        ax.set_aspect("equal", adjustable="datalim")

    cell_handles = [
        Patch(facecolor=_color_for(q), edgecolor="none", label=_label_for(q))
        for q in ALL_CELLS_ORDER
    ]
    frame_handles = [
        Line2D([0], [0], marker="o", color="w", markerfacecolor="#666",
               markeredgecolor="black", markersize=9,
               label=f"mirror (n={n_m})"),
        Line2D([0], [0], marker="^", color="w", markerfacecolor="#666",
               markeredgecolor="black", markersize=9,
               label=f"self_event (n={n_s})"),
        Line2D([0], [0], marker="s", color="w", markerfacecolor="#666",
               markeredgecolor="black", markersize=9,
               label=f"true_self (n={n_t})"),
        Line2D([0], [0], color="#888888", lw=1.0,
               label="m → se shift"),
        Line2D([0], [0], color="#1a5f6f", lw=1.5,
               label="m → ts shift"),
    ]
    fig.legend(handles=cell_handles, loc="center right",
               bbox_to_anchor=(0.995, 0.66), fontsize=8,
               title="cell", title_fontsize=9, frameon=True)
    fig.legend(handles=frame_handles, loc="center right",
               bbox_to_anchor=(0.995, 0.27), fontsize=8,
               title="frame / shift", title_fontsize=9, frameon=True)

    fig.suptitle(
        f"{which} layer-stack PCA — {short_name}\n"
        f"mirror vs self_event vs true_self in shared basis  "
        f"(cum PC1+PC2 = {(var[0]+var[1])*100:.1f}%)",
        fontsize=12,
    )
    fig.tight_layout(rect=(0, 0, 0.86, 0.95))
    fig.savefig(out_path, dpi=150, bbox_inches="tight")
    plt.close(fig)


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--model", default=None,
                        help="Short name (default: $LLMOJI_MODEL or gemma).")
    parser.add_argument("--which", choices=("h_first", "h_last", "h_mean"),
                        default="h_first")
    parser.add_argument("--self-event-suffix", default="self_event")
    parser.add_argument("--true-self-suffix", default="true_self")
    parser.add_argument("--n-components", type=int, default=8)
    args = parser.parse_args()

    import os
    short = args.model or os.environ.get("LLMOJI_MODEL", "gemma")
    print(f"model: {short}  which={args.which}")
    print(f"  self_event suffix: {args.self_event_suffix!r}")
    print(f"  true_self suffix:  {args.true_self_suffix!r}")

    M_mirror = _resolve(short, suffix=None)
    M_self = _resolve(short, suffix=args.self_event_suffix)
    M_ts = _resolve(short, suffix=args.true_self_suffix)

    bundles = []
    for tag, M in (("mirror", M_mirror), ("self_event", M_self),
                   ("true_self", M_ts)):
        print(f"\n[{tag}]  {M.emotional_data_path}")
        b = _load_frame(M, args.which)
        if b is None:
            print(f"no {tag} frame data; aborting")
            sys.exit(1)
        df, X3, layers = b
        print(f"  {len(df)} rows × {X3.shape[1]} layers × "
              f"{X3.shape[2]} hidden_dim")
        bundles.append((tag, df, X3, layers))

    (_, df_m, X3_m, layers_m) = bundles[0]
    (_, df_s, X3_s, layers_s) = bundles[1]
    (_, df_t, X3_t, layers_t) = bundles[2]

    sel_m, sel_s, sel_t, common = _common_layers_three(
        layers_m, layers_s, layers_t,
    )
    print(f"\nlayer intersection: |m|={len(layers_m)} ∩ "
          f"|s|={len(layers_s)} ∩ |t|={len(layers_t)} → "
          f"{len(common)} common")
    X3_m, X3_s, X3_t = X3_m[:, sel_m, :], X3_s[:, sel_s, :], X3_t[:, sel_t, :]

    n_m, L, H = X3_m.shape
    n_s = X3_s.shape[0]
    n_t = X3_t.shape[0]
    X_m = X3_m.reshape(n_m, L * H)
    X_s = X3_s.reshape(n_s, L * H)
    X_t = X3_t.reshape(n_t, L * H)
    X = np.concatenate([X_m, X_s, X_t], axis=0)

    n_components = min(args.n_components, X.shape[0], X.shape[1])
    print(f"\nfitting PCA(n_components={n_components}) on combined "
          f"{X.shape[0]} rows × {X.shape[1]} feats...")
    pca = PCA(n_components=n_components)
    pca.fit(X)
    var = pca.explained_variance_ratio_
    print("  explained-variance spectrum:")
    for k, v in enumerate(var, 1):
        print(f"    PC{k}: {v*100:6.2f}%  "
              f"(cumulative {var[:k].sum()*100:5.2f}%)")

    coords_m = pca.transform(X_m)
    coords_s = pca.transform(X_s)
    coords_t = pca.transform(X_t)

    q_m = df_m["quadrant"].to_numpy()
    q_s = df_s["quadrant"].to_numpy()
    q_t = df_t["quadrant"].to_numpy()
    cent_m = _centroids_in_pca(coords_m, q_m, QUADRANT_ORDER_SPLIT)
    cent_s = _centroids_in_pca(coords_s, q_s, ALL_CELLS_ORDER)
    cent_t = _centroids_in_pca(coords_t, q_t, ALL_CELLS_ORDER)

    print("\nrow counts by cell:")
    print(f"  {'cell':>6s}  {'mirror':>7s}  {'self_ev':>8s}  {'true_self':>10s}")
    for q in ALL_CELLS_ORDER:
        nm = int((q_m == q).sum())
        ns = int((q_s == q).sum())
        nt = int((q_t == q).sum())
        print(f"  {_label_for(q):>6s}  {nm:>7d}  {ns:>8d}  {nt:>10d}")

    # === High-dim deltas (in the original layer-stack space) ===
    # Per-cell centroid in the layer-stack; produces unit deltas free
    # of PCA distortion. The PCA-projected deltas live in the figures.
    cent_m_hd: dict[str, np.ndarray] = {}
    cent_s_hd: dict[str, np.ndarray] = {}
    cent_t_hd: dict[str, np.ndarray] = {}
    for q in QUADRANT_ORDER_SPLIT:
        mask = q_m == q
        if np.any(mask):
            cent_m_hd[q] = X_m[mask].mean(axis=0)
    for q in ALL_CELLS_ORDER:
        mask = q_s == q
        if np.any(mask):
            cent_s_hd[q] = X_s[mask].mean(axis=0)
    for q in ALL_CELLS_ORDER:
        mask = q_t == q
        if np.any(mask):
            cent_t_hd[q] = X_t[mask].mean(axis=0)

    delta_se_minus_m: dict[str, np.ndarray] = {}
    delta_ts_minus_m: dict[str, np.ndarray] = {}
    for q in QUADRANT_ORDER_SPLIT:
        if q in cent_m_hd and q in cent_s_hd:
            delta_se_minus_m[q] = cent_s_hd[q] - cent_m_hd[q]
        if q in cent_m_hd and q in cent_t_hd:
            delta_ts_minus_m[q] = cent_t_hd[q] - cent_m_hd[q]

    coh_se, std_se = _delta_coherence(delta_se_minus_m)
    coh_ts, std_ts = _delta_coherence(delta_ts_minus_m)

    print("\nper-cell delta coherence (mean pairwise cosine across cells):")
    print(f"  (self_event − mirror)   : {coh_se:+.3f} ± {std_se:.3f}  "
          f"(self.other axis, n={len(delta_se_minus_m)})")
    print(f"  (true_self  − mirror)   : {coh_ts:+.3f} ± {std_ts:.3f}  "
          f"(speaker+content shifted, n={len(delta_ts_minus_m)})")

    print("\nper-cell magnitude (|delta|) and ratio:")
    print(f"  {'cell':>6s}  {'|se−m|':>8s}  {'|ts−m|':>8s}  "
          f"{'ratio (ts/se)':>14s}")
    ratios: list[float] = []
    for q in QUADRANT_ORDER_SPLIT:
        ds = delta_se_minus_m.get(q)
        dt = delta_ts_minus_m.get(q)
        if ds is None or dt is None:
            continue
        nse = float(np.linalg.norm(ds))
        nts = float(np.linalg.norm(dt))
        r = nts / nse if nse > 0 else float("nan")
        if not np.isnan(r):
            ratios.append(r)
        print(f"  {q:>6s}  {nse:>8.3f}  {nts:>8.3f}  {r:>14.3f}")
    if ratios:
        print(f"  median ratio: {np.median(ratios):.3f}  "
              f"(predicted 1.3-2× — content-shift + speaker-shift compound)")

    # Also: cosine alignment of the (ts − m) and (se − m) deltas at
    # each cell — does prefill push the same direction self.other does?
    print("\nper-cell cosine(ts−m, se−m):")
    cosines: list[float] = []
    for q in QUADRANT_ORDER_SPLIT:
        ds = delta_se_minus_m.get(q)
        dt = delta_ts_minus_m.get(q)
        if ds is None or dt is None:
            continue
        c = float(np.dot(ds, dt) / (np.linalg.norm(ds) * np.linalg.norm(dt)))
        cosines.append(c)
        print(f"  {q:>6s}  {c:+.3f}")
    if cosines:
        print(f"  mean: {np.mean(cosines):+.3f}  std: {np.std(cosines):.3f}")

