"""Three-way PCA scatter — mirror, self-event, and true-self in a shared basis.

Extends ``22h_combined_pca_scatter.py`` to a third elicitation frame:
the assistant-prefilled introspective self-report set generated by
``00_emit_true_self.py``. Loads all three frames for one model,
fits a single PCA on the combined layer-stacked ``h_first`` matrix,
projects each frame into the shared basis, and writes:

* ``fig_three_way_pca_scatter_<which>.png``       — PC1×PC2 + PC2×PC3
* ``fig_three_way_pca_scatter_3d_<which>.html``   — interactive PC1..3

Marker shapes encode the frame:

  circle    — mirror (user is speaker, event happens to user)
  triangle  — self-event (user is speaker, event about model)
  square    — true-self (model is speaker via prefill, content about model)

Per-cell centroids are connected with two sets of frame-shift
segments:

  light gray   — mirror → self-event   (existing self.other axis)
  dark blue    — mirror → true-self    (new — speaker AND content shifted)

Stdout reports three numerical comparisons:

  1. Mean per-cell coherence of ``(self_event − mirror)`` delta
     vectors (canonical self.other coherence; replicates 22g).
  2. Mean per-cell coherence of ``(true_self − mirror)`` delta
     vectors (new — the prediction is ≥ +0.73, matching or exceeding
     self.other).
  3. Per-cell magnitude ratio
     ``|true_self − mirror| / |self_event − mirror|``. Predicted
     1.3-2× under the speaker-shift-and-content-shift compound;
     <1 would invert the self.other direction interpretation.

Usage::

    LLMOJI_MODEL=gemma python scripts/local/22i_three_way_pca_scatter.py
    LLMOJI_MODEL=gemma python scripts/local/22i_three_way_pca_scatter.py --which h_last
"""

