#!/usr/bin/env bash
# Regenerate every figure embedded in the introspection-via-kaomoji
# blog post. Outputs land under
# ../a9lim.github.io/blog-assets/introspection-via-kaomoji/.
#
# Two layers:
#
#   1. **Static PNGs** (12 files: 5-model kaomoji-cosine heatmap +
#      1 layerwise emergence comparison, both in light/dark themes).
#      Driven by scripts/local/99_regen_blog_figures.py — reads
#      sidecars + h_first cache; no new model time. Auto-themed via
#      a9l.im/shared-tokens.js light/dark surface vars.
#
#   2. **Interactive 3D HTMLs** (3 files: per-face PCA, quadrant
#      procrustes, wild-faces PCA). Source HTMLs in figures/local/
#      and figures/harness/ are wrapped with blog-side styling +
#      controls by scripts/local/98_wrap_blog_3d_html.py. Source
#      HTMLs themselves are regenerated by their producing scripts:
#
#        figures/local/fig_v3_quadrant_procrustes_3d.html
#          ← scripts/local/26_v3_quadrant_procrustes.py
#        figures/local/fig_v3_per_face_pca_3d.html
#          ← scripts/local/97_build_per_face_pca_3d.py
#        figures/harness/wild_faces_pca_3d.html
#          ← scripts/67_wild_residual.py --color-by gt
#
# Pass --skip-3d to skip the 3D-source regen + wrap (use when the
# blog edit only touches static figures or you've manually staged
# the 3D HTMLs). Pass --skip-static to skip the PNG regen.
#
# Usage:
#   scripts/regen_blog.sh                    # full regen
#   scripts/regen_blog.sh --skip-3d          # PNGs only
#   scripts/regen_blog.sh --skip-static      # 3D HTMLs only
#
# Prereqs: per-model emit + hidden-state analyses must have run
# (see scripts/run_local_chain.sh). The 3D producer scripts also
# need data/v3_face_union.parquet (built by stage 2) and
# data/harness/claude_faces_lexicon_bag.parquet (built by the
# harness chain).

set -euo pipefail
cd "$(dirname "$0")/.."

skip_3d=0
skip_static=0
for arg in "$@"; do
    case "$arg" in
        --skip-3d)     skip_3d=1 ;;
        --skip-static) skip_static=1 ;;
        -h|--help)
            sed -n '2,/^$/p' "$0" | sed 's/^# \?//'
            exit 0
            ;;
        *)
            echo "unknown flag: $arg" >&2
            echo "usage: scripts/regen_blog.sh [--skip-3d] [--skip-static]" >&2
            exit 2
            ;;
    esac
done

echo ""
echo "################################################################"
echo "#  blog-figures regen"
echo "################################################################"

# ---------------------------------------------------------------------
# Static PNGs.
# ---------------------------------------------------------------------
if [ "$skip_static" = 0 ]; then
    echo ""
    echo "================================================================"
    echo "  stage 1: static PNGs (heatmaps + layerwise comparison)"
    echo "================================================================"
    echo ""
    echo "  >>> scripts/local/99_regen_blog_figures.py"
    .venv/bin/python scripts/local/99_regen_blog_figures.py
fi

# ---------------------------------------------------------------------
# Interactive 3D HTMLs.
# ---------------------------------------------------------------------
if [ "$skip_3d" = 0 ]; then
    echo ""
    echo "================================================================"
    echo "  stage 2: regenerate raw 3D HTMLs"
    echo "================================================================"

    # Quadrant procrustes — drops fig_v3_quadrant_procrustes_3d.html
    # under figures/local/. Uses default 5-model lineup with gemma as
    # reference; idempotent.
    echo ""
    echo "  >>> scripts/local/26_v3_quadrant_procrustes.py"
    .venv/bin/python scripts/local/26_v3_quadrant_procrustes.py \
        --models gemma,qwen,ministral,gpt_oss_20b,granite \
        --reference gemma

    # Per-face PCA — drops fig_v3_per_face_pca_3d.html + meta.json.
    echo ""
    echo "  >>> scripts/local/97_build_per_face_pca_3d.py"
    .venv/bin/python scripts/local/97_build_per_face_pca_3d.py

    # Wild-faces PCA in BoL space, colored by per-face BoL quadrant
    # blend (default --color-by bol) — drops wild_faces_pca_3d.html
    # under figures/harness/. Matches the blog post caption ("Per-face
    # color is a proportional RGB-blend of BoL quadrant shares") and
    # the path 98_wrap_blog_3d_html.py reads. The --color-by gt /
    # --color-by predicted variants are companion views, not the
    # blog's primary figure.
    echo ""
    echo "  >>> scripts/67_wild_residual.py --fixed-k 6"
    .venv/bin/python scripts/67_wild_residual.py --fixed-k 6

    echo ""
    echo "================================================================"
    echo "  stage 3: wrap 3D HTMLs with blog styling"
    echo "================================================================"
    echo ""
    echo "  >>> scripts/local/98_wrap_blog_3d_html.py"
    .venv/bin/python scripts/local/98_wrap_blog_3d_html.py
fi

echo ""
echo "################################################################"
echo "#  blog-figures regen complete"
echo "#"
echo "#  outputs: ../a9lim.github.io/blog-assets/introspection-via-kaomoji/"
echo "################################################################"
